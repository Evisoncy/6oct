package PMTD;

/**
 *
 * @author WIMBER
 */
public class hechos {
    private String hechos;
    private String supo;
    private String justi;

    public hechos(String hechos, String supo, String justi) {
        this.hechos = hechos;
        this.supo = supo;
        this.justi = justi;
    }

    public String getHechos() {
        return hechos;
    }

    public void setHechos(String hechos) {
        this.hechos = hechos;
    }

    public String getSupo() {
        return supo;
    }

    public void setSupo(String supo) {
        this.supo = supo;
    }

    public String getJusti() {
        return justi;
    }

    public void setJusti(String justi) {
        this.justi = justi;
    }
    
    
}
