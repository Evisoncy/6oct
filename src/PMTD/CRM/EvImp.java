    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        try {
            if (rbtnSi.isSelected() == true) {
                clips.eval("(assert (opcion si))");
                System.out.println("si");
            }

            if (rbtnNo.isSelected() == true) {
                clips.eval("(assert (opcion no))");
                System.out.println("no");
            }

            clips.run();

            String evaluar = "(find-all-facts ((?x nodo-pregunta-respuesta )) TRUE)";
            FactAddressValue value = (FactAddressValue) ((MultifieldValue) clips.eval(evaluar)).get(0);
            String tipo = value.getFactSlot("tipo").toString();
            System.out.println(tipo);
            String valor = value.getFactSlot("valor").toString();
            System.out.println(valor);
            if (tipo.equals("decision")) {
                lblPregunta.setText(valor);
                lblRespuesta.setText("");
            } else {
                if(tipo.equals("respuesta")){

                    lblPregunta.setText("");
                    lblRespuesta.setText("Desea volver a intentarlo?");

                    JOptionPane.showMessageDialog(null,valor , "Mensaje",JOptionPane.INFORMATION_MESSAGE);
                    res.setText(valor);
                }else{
                    //System.exit(0);
                    
                }

            }
            buttonGroup1.clearSelection();
        } catch (Exception ex) {
            Logger.getLogger(EvProb.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnContinuarActionPerformed

